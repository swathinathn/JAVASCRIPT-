let x=10;
let y=20;

//Equal (==)
console.log("after x==y:",x==y);


//Not Equal (!=)
console.log("after x!=y:",x!=y);


//Strict Equal (===)
console.log("after x===y:",x===y);


//Greater Than (>)
console.log("after x>y:",x>y);

//Less Than (<)
console.log("after x<y:",x<y);

//Greater Than or Equal To (>=)
console.log("after x>=y:",x>=y);

//Less Than or Equal To (<=)
console.log("after x<=y:",x<=y);





